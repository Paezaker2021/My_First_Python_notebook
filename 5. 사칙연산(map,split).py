a,b=map(int,input("a,b 값 입력:").split())
print("a+b=",a+b)
print("a-b=",a-b)
print("a*b=",a*b)
print("a/b=",a/b)   #몫
print("a//b=",a//b) #몫의 정수 형태만 출력
print("a%b=",a%b) #나머지
print("a**b=",a**b) #제곱



#map(a,b) b를 a형태로 변환시키는 것/입력값이 2개 이상일 때 사용함.
#=의 왼쪽 값의 갯수 == 입력받는 갯수
#.split() : a,b 입력값을 나누어주는 기준. () = 띄어쓰기 / (,) = ,로 a와 b를 구분함.
